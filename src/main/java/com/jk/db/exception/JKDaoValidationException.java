package com.jk.db.exception;

public class JKDaoValidationException extends JKDaoException {

	public JKDaoValidationException() {
		super();
	}

	public JKDaoValidationException(String message, Throwable cause) {
		super(message, cause);
	}

	public JKDaoValidationException(String message) {
		super(message);
	}

	public JKDaoValidationException(Throwable cause) {
		super(cause);
	}

}
